public class School1 {

    private String name;
    private String description;
    private int numberOfNumberClassrooms;
    private double squareFootage;
    private int studentCapacity;
    private Cafeteria cafeteria;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public int getNumberOfNumberClassrooms() {
        return numberOfNumberClassrooms;
    }

    public void setNumberOfNumberClassrooms(int numberOfNumberClassrooms) {
        this.numberOfNumberClassrooms = numberOfNumberClassrooms;
    }

    public double getSquareFootage() {
        return squareFootage;
    }

    public void setSquareFootage(double squareFootage) {
        this.squareFootage = squareFootage;
    }

    public int getStudentCapacity() {
        return studentCapacity;
    }

    public void setStudentCapacity(int studentCapacity) {
        this.studentCapacity = studentCapacity;
    }

    public Cafeteria getCafeteria() {
        return cafeteria;
    }

    public void setCafeteria(Cafeteria cafeteria) {
        this.cafeteria = cafeteria;
    }
}
